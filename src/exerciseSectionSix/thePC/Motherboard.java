package exerciseSectionSix.thePC;

public class Motherboard {
    private String model;
    private String manufacturer;
    private int slots;
    private int ram;
    private String bios;

    public Motherboard(String model, String manufacturer, int slots, int ram, String bios) {
        this.model = model;
        this.manufacturer = manufacturer;
        this.slots = slots;
        this.ram = ram;
        this.bios = bios;
    }


}
